//transfomer un [][] en int**
int** trans(int carte_id[NOMBRE_TILE_HAUTEUR][NOMBRE_TILE_LARGEUR]){
    int** c = malloc(NOMBRE_TILE_HAUTEUR*sizeof(int*));
    for (int i=0; i<NOMBRE_TILE_HAUTEUR; i++){
        c[i] = malloc(NOMBRE_TILE_LARGEUR*sizeof(int));
        for (int j=0; j<NOMBRE_TILE_LARGEUR; j++){
            c[i][j] = carte_id[i][j];
        }
    }
    return c;
}


